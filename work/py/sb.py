# |이 코드는 3개의 숫자를 맞추는 숫자 야구 게임을 구현한 코드입니다.
# |
# |좋은 점:
# |- 랜덤 모듈을 사용하여 컴퓨터가 생성한 숫자를 무작위로 생성합니다.
# |- 사용자가 입력한 숫자를 검증하여 중복되는 숫자가 없고 3개의 숫자가 입력되었는지 확인합니다.
# |- 사용자가 입력한 숫자와 컴퓨터가 생성한 숫자를 비교하여 스트라이크와 볼의 개수를 계산합니다.
# |- 게임이 종료되었는지 여부를 확인하고, 종료되지 않았다면 다시 입력을 받습니다.
# |
# |나쁜 점:
# |- 코드의 가독성이 떨어집니다. 변수명이 짧고 의미가 명확하지 않아 코드를 이해하기 어렵습니다.
# |- 중복되는 코드가 있습니다. 예를 들어, 사용자가 입력한 숫자를 검증하는 부분과 스트라이크와 볼의 개수를 계산하는 부분에서 중복되는 코드가 있습니다. 이러한 중복 코드는 함수로 분리하여 코드의 가독성을 높일 수 있습니다.
# |- 코드의 유연성이 떨어집니다. 현재 코드는 3개의 숫자를 맞추는 게임에만 적용됩니다. 만약 숫자의 개수를 변경하고 싶다면 코드를 수정해야 합니다. 이러한 유연성을 높이기 위해서는 변수를 사용하여 숫자의 개수를 동적으로 변경할 수 있도록 코드를 작성해야 합니다.
import random

com = []
while len(com) < 3:
    r = random.randint(0,9)
    if r not in com:
        com.append(r)

while True:
    ui = input("숫자 3개 입력 : ")
    if len(ui) != 3:
        continue
    user = []
    for i in range(3):
        if int(ui[i]) not in user:
            user.append(int(ui[i]))
    if len(user) !=3:
        continue

    s, b = 0, 0

    for x in range(3):
        for y in range(3):
            if com[x] == user[y]:
                if x==y:
                    s+=1
                else:
                    b+=1

    if s ==3:
        print("3 s")
        break
    elif s ==0 and b ==0:
        print("out")
    else:
        print(s,"s\t",b,"b")